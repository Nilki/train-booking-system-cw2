
public class Passenger {

    private String firstName;
    private String lastName;
    private int secondsInQueue;

    public String getFirstName() { //getter firstName
        return firstName;
    }

    public void setFirstName(String firstName) {    //setter firstName
        this.firstName = firstName;
    }

    public String getLastName() {  //getter lastName
        return lastName;
    }

    public void setLastName(String lastName) {          //setter lastName
        this.lastName = lastName;
    }

    public int getSecondsInQueue() {   //getter SecondsInQueue
        return secondsInQueue;
    }

    public void setSecondsInQueue(int secondsInQueue) {     //setter SecondsInQueue
        this.secondsInQueue = secondsInQueue;
    }
}






